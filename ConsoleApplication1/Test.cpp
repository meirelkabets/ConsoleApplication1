#include "Test.h"

void TaskStarter(CTest *pvArg)
{	
	pvArg->Task();
}

///////////////////////////////////////////////////////////////////////////////
CTest::CTest(void)
{
	m_tskStarted = false;
	m_pTask      = NULL;
}

///////////////////////////////////////////////////////////////////////////////
CTest::~CTest(void)
{
	if (m_pTask)
	{
		m_pTask->join();
		delete m_pTask;
	}
}

///////////////////////////////////////////////////////////////////////////////
void CTest::Initialize()
{
	m_pTask = new std::thread(TaskStarter, this);	
}

///////////////////////////////////////////////////////////////////////////////
void CTest::Task()
{
	m_tskStarted = true;

	while (m_tskStarted)
	{
		int y = 100;
	}
}
